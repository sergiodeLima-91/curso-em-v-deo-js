/* Estrutura de Repetição com Variável de Controle: HEXÁGONO REGULAR 

1 - Inicialização
2 - Teste Lógico
3 - Incremento

No momento do looping é feito o incremento (na volta)

for (inicio; teste lógico; incremento){
    (funcionalidade)
}

*/

for (let Inicialização = 1; Inicialização <= 10; Inicialização++) {
    console.log(`Passo ${Inicialização}`);
}